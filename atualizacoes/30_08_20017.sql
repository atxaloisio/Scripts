CREATE TABLE `filial_logo` (
  `Id` bigint(20) NOT NULL AUTO_INCREMENT,
  `id_filial` bigint(20) NOT NULL,
  `imagem` blob,
  PRIMARY KEY (`Id`),
  KEY `fk_filial_logo_filial` (`Id_filial`),
  CONSTRAINT `fk_filial_logo_filial` FOREIGN KEY (`Id_filial`) REFERENCES `filial` (`Id`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=latin1 COLLATE=latin1_general_cs;
