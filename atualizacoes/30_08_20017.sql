  `complemento` varchar(60) COLLATE latin1_general_cs DEFAULT NULL,
  `bairro` varchar(30) COLLATE latin1_general_cs DEFAULT NULL,
  `cidade` varchar(40) COLLATE latin1_general_cs DEFAULT NULL,
